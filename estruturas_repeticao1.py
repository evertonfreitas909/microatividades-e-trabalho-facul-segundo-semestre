# Letra A: criar variável entrada_idade (string inicial vazia)
entrada_idade = ''

# Letra B: quando for diferente de "0"
while entrada_idade != "0":
    # Letra C: pedir entrada de dados
    entrada_idade = input('Digite um número qualquer ou zero para sair: ')

    # Letra D: imprimir o valor digitado
    print('Número digitado: ', entrada_idade)

