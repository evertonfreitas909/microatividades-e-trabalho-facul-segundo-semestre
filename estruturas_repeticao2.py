# A. Crie a variável 'texto'
texto = 'Olá, laço for.'
# B. Criar a intrução for que percorre cada caracter
for item in texto:
    # C. imprimir o caractere precedido do texto 'caractere:'
    print('caracteres:', item)
# Nova instrução for
# A. iterar sobre um intervalo númerico entre 1 e 10
for numero in range(1, 11):
    # B e C. Imprimir o número convertido em string
    print('número do intervalo: ' + str(numero))